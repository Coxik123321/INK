def generate_epb(conclusion, resource_years):
    if conclusion == "Эксплуатация допускается":
        return f"""
Экспертиза промышленной безопасности показала,
что объект может эксплуатироваться
при соблюдении регламентных параметров.
Остаточный ресурс: {resource_years} лет.
"""
    if conclusion == "Усиленный контроль":
        return f"""
Эксплуатация допускается при ограничениях
и усиленном мониторинге.
Остаточный ресурс: {resource_years} лет.
"""
    return """
Эксплуатация объекта не допускается.
Требуется проведение ремонтных работ
или вывод из эксплуатации.
"""
